function buildTicker() {
  const wrap = document.querySelector('.ticker-scroll');
  if (!wrap) return;
  const items = [...TICKER_DATA, ...TICKER_DATA].map(t =>
    `<span class="ticker-item"><span class="ticker-sym">${t.sym}</span><span class="ticker-price">${t.price}</span><span class="ticker-change ${t.pos ? 'pos' : 'neg'}">${t.change}</span></span>`
  ).join('');
  wrap.innerHTML = items;
}

function initNav() {
  const toggle = document.querySelector('.nav-mobile-toggle');
  const links = document.querySelector('.nav-links');
  if (toggle && links) toggle.addEventListener('click', () => links.classList.toggle('open'));
}

function initAccordions() {
  document.querySelectorAll('.accordion-header').forEach(header => {
    header.addEventListener('click', () => {
      const body = header.nextElementSibling;
      const isOpen = header.classList.contains('open');
      header.classList.toggle('open', !isOpen);
      if (body) body.classList.toggle('open', !isOpen);
      const chevron = header.querySelector('.accordion-chevron');
      if (chevron) chevron.style.transform = !isOpen ? 'rotate(180deg)' : '';
    });
  });
}

document.addEventListener('DOMContentLoaded', () => {
  buildTicker();
  buildLangSwitcher();
  initNav();
  initAccordions();
  applyLang(currentLang);
});
